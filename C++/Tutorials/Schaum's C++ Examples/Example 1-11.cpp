#include <iostream>
using namespace std;
int main()
{
    int m, n;
    cout << "Enter two integers: ";
    cin >> m >> n;
    cout << "m = " << m << ", n = " << n << endl;
    double x, y, z;
    cout << "Enter the decimal numbers: ";
    cin >> x >> y >> z;
    cout << "x = " << x << ", y = " << y << ", z = " << z << endl;
    char c1, c2, c3, c4;
    cout << "Enter four characters: ";
    cin >> c1 >> c2 >> c3 >> c4;
    cout << "c1 = " << c1 << ", c2 = " << c2 << ", c3 = " << c3 << ", c4 = " << c4 << endl;
}
